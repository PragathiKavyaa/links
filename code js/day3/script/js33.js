let day="monday";
switch (day) {
    case "monday":
        console.log("Have a magical day");
        break;

    case"tuesday":
        console.log("Have a treat day");
        break;

    case"wednesday":
        console.log("Have a weak day");
        break;

    case"thursday":
        console.log("Have a thirsty day");
        break;

    case"friday":
        console.log("Have a free day");
        break;

    case"saturday":
        console.log("Have a slave day");
        break;

    case"sunday":
        console.log("Have a safe day");
        break;

    default:
        console.log("Invalid");
        
        break;
}